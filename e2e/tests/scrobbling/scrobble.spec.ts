// ── Edge cases — Navidrome only ────────────────────────────────────────────
// The loop-detection logic (PlayerBar.tsx mpvRestartCount / Player.tsx ended-event
// guard) is server-type-agnostic — the per-backend difference is only which HTTP
// endpoint is called, not whether to call it. Jellyfin edge-case coverage deferred;
// add Jellyfin describe blocks here if a Jellyfin-specific regression is ever observed.

async function runRepeatAllScrobblesEachLoop(window: Page) {
  let scrobbleCount = 0;
  await window.route('**/*', async (route) => {
    if (isRealScrobble(route.request().url())) scrobbleCount++;
    await route.continue();
  });

  await window.click('[data-testid="nav-albums"]');
  await expect(window.locator(`text=${TRACKS.soloTrack.album}`).first()).toBeVisible({
    timeout: 15_000,
  });
  // Albums require a double-click to navigate in — a single click only selects
  // the row (see AlbumList.tsx's useListClickHandler: doubleClick navigates,
  // there is no singleClick override).
  await window.locator(`text=${TRACKS.soloTrack.album}`).first().dblclick();
  await window.locator(`text=${TRACKS.soloTrack.title}`).first().dblclick();
  await window.waitForSelector('[data-testid="player-bar"]');

  const repeatBtn = window.locator('[data-testid="player-repeat"]');
  for (let i = 0; i < 4; i++) {
    if ((await repeatBtn.getAttribute('data-repeat-mode')) === 'all') break;
    await repeatBtn.click();
    await window.waitForTimeout(200);
  }

  await window.waitForTimeout((TRACKS.soloTrack.durationSeconds + 5) * 1000);
  expect(scrobbleCount).toBeGreaterThanOrEqual(1);
  await window.waitForTimeout((TRACKS.soloTrack.durationSeconds + 5) * 1000);
  expect(scrobbleCount).toBeGreaterThanOrEqual(2);
}

